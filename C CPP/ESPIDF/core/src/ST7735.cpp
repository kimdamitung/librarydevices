bool ST7735::spi_bus_initialized = false;

void ST7735::select(){
    gpio_set_level(this->cs, false);
}

void ST7735::unselect(){
    gpio_set_level(this->cs, true);
}

void ST7735::reset(){
    gpio_set_level(this->rst, false);
    vTaskDelay(10 / portTICK_PERIOD_MS);
    gpio_set_level(this->rst, true);
    vTaskDelay(10 / portTICK_PERIOD_MS);
}

void ST7735::write(uint8_t commands){
    gpio_set_level(this->dc, false);
    vTaskDelay(5 / portTICK_PERIOD_MS);
    spi_transaction_t t = {
        .length = sizeof(commands),
        .tx_buffer = &commands,
        .rx_buffer = nullptr
    };
    esp_err_t ret = spi_device_transmit(this->spi, &t);
    if (ret != ESP_OK) {
        printf("Error transmitting command: %s\n", esp_err_to_name(ret));
    }
}

void ST7735::write(const uint8_t *buffer, size_t size){
    gpio_set_level(this->dc, true);
    spi_transaction_t t = {
        .length = size,
        .tx_buffer = buffer,
        .rx_buffer = nullptr
    };
    esp_err_t ret = spi_device_transmit(this->spi, &t);
    if (ret != ESP_OK) {
        printf("Error transmitting data: %s\n", esp_err_to_name(ret));
    }
}

void ST7735::commands(const uint8_t *address){
    if (address == nullptr)
        return;
    uint8_t numbers = *address++;
    while (numbers--) {
        uint8_t command = *address++;
        this->write(command);
        uint8_t arguments = *address++;
        uint16_t miliseconds = arguments & DELAY;
        arguments &= ~DELAY;
        if (arguments) {
            this->write(address, arguments);
            address += arguments;
        }
        if (miliseconds) {
            miliseconds = *address++;
            if (miliseconds == 255)
                miliseconds = 500;
            vTaskDelay(miliseconds / portTICK_PERIOD_MS);
        }
    } 
}

void ST7735::setAddressWindows(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1){
    this->commands((const uint8_t*)this->CASET);
    uint8_t data[] = {0x00, (uint8_t)(x0 + this->XSTART), 0x00, (uint8_t)(x1 + this->XSTART)};
    this->write(data, sizeof(data));
    this->commands((const uint8_t*)this->RASET);
    data[1] = (uint8_t)(y0 + this->YSTART);
    data[3] = (uint8_t)(y1 + this->YSTART);
    this->write(data, sizeof(data));
    this->commands((const uint8_t*)this->RAMWR);
}

ST7735::ST7735(gpio_num_t cs, gpio_num_t dc, gpio_num_t rst, spi_host_device_t host, gpio_num_t mosi, gpio_num_t sck, gpio_num_t miso) {
    this->cs = cs;
    this->dc = dc;
    this->rst = rst;
    this->host = host;
    esp_rom_gpio_pad_select_gpio(this->cs);
    esp_rom_gpio_pad_select_gpio(this->dc);
    esp_rom_gpio_pad_select_gpio(this->rst);
    gpio_set_direction(this->cs, GPIO_MODE_OUTPUT);
    gpio_set_direction(this->dc, GPIO_MODE_OUTPUT);
    gpio_set_direction(this->rst, GPIO_MODE_OUTPUT);
    if (!spi_bus_initialized) {
        spi_bus_config_t buscfg = {
            .mosi_io_num = mosi,
            .miso_io_num = miso,
            .sclk_io_num = sck,
            .quadwp_io_num = -1,
            .quadhd_io_num = -1,
            .max_transfer_sz = (8192 / 2),
            .flags = 0,
            .intr_flags = 0
        };
        esp_err_t ret = spi_bus_initialize(this->host, &buscfg, false);
        if (ret != ESP_OK) {
            printf("Error initializing SPI bus: %s\n", esp_err_to_name(ret));
            return;
        }
        spi_bus_initialized = true;
    }
    spi_device_interface_config_t dev_config = {
        .command_bits = 0,
        .address_bits = 0,
        .dummy_bits = 0,
        .mode = 0,
        .duty_cycle_pos = 128,
        .cs_ena_pretrans = 0,
        .cs_ena_posttrans = 0,
        .clock_speed_hz = 50000,
        .input_delay_ns = 0,
        .spics_io_num = this->cs,
        .flags = 0,
        .queue_size = 7,
        .pre_cb = NULL,
        .post_cb = NULL 
    };
    esp_err_t ret = spi_bus_add_device(this->host, &dev_config, &this->spi);
    if (ret != ESP_OK) {
        printf("Error adding SPI device: %s\n", esp_err_to_name(ret));
    }
    this->select();
    this->reset();
    this->commands(init_cmds1);
    this->commands(init_cmds2);
    this->commands(init_cmds3);
    this->unselect();
}

ST7735::~ST7735() {
    if (this->spi) {
        spi_bus_remove_device(this->spi);
        this->spi = NULL;
    }
    if (spi_bus_initialized) {
        spi_bus_free(this->host);
        spi_bus_initialized = false;
    }
}

void ST7735::drawPixels(uint16_t x, uint16_t y, uint16_t color){
    if((x >= this->WIDTH) || (y >= this->HEIGHT))
        return;
    this->select();
    this->setAddressWindows(x, y, x + 1, y + 1);
    uint8_t data[] = {(uint8_t)(color >> 8), (uint8_t)(color & 0xFF)};
    this->write(data, sizeof(data));
    this->unselect();
}

void ST7735::characters(uint16_t x, uint16_t y, char character, fonts font, uint16_t color, uint16_t background){
    uint32_t i, b, j;
    this->setAddressWindows(x, y, x + font.width - 1, y + font.height - 1);
    for(i = 0; i < font.height; i++) {
        b = font.data[(character - 32) * font.height + i];
        for(j = 0; j < font.width; j++) {
            if((b << j) & 0x8000)  {
                uint8_t data[] = {(uint8_t)(color >> 8), (uint8_t)(color & 0xFF)};
                this->write(data, sizeof(data));
            } else {
                uint8_t data[] = {(uint8_t)(background >> 8), (uint8_t)(background & 0xFF)};
                this->write(data, sizeof(data));
            }
        }
    }
}

void ST7735::strings(uint16_t x, uint16_t y, const char* string, fonts font, uint16_t color, uint16_t background){
    this->select();
    while (*string){        
        /* code */
        if(x + font.width >= this->WIDTH){
            x = 0;
            y += font.height;
            if(y + font.height >= this->HEIGHT)
                break;
            if(*string == ' '){
                string++;
                continue;
            }
        }
        this->characters(x, y, *string, font, color, background);
        x += font.width;
        string++;
    }
    this->unselect();
}

void ST7735::rectangle(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color){
    if((x >= this->WIDTH) || (y >= this->HEIGHT))
        return;
    if((x + w - 1) >= this->WIDTH)
        w = this->WIDTH - x;
    if((y + h - 1) >= this->HEIGHT)
        h = this->HEIGHT - y;
    this->select();
    this->setAddressWindows(x, y, x + w - 1, y + h - 1);
    uint8_t data[] = {(uint8_t)(color >> 8), (uint8_t)(color & 0xFF)};
    gpio_set_level(this->dc, true);
    spi_transaction_t t = {
        .length = sizeof(data),
        .tx_buffer = data,
        .rx_buffer = nullptr
    };
    for(y = h; y > 0; y--){
        for(x = w; x > 0; x--){
            spi_device_transmit(this->spi, &t);
        }
    }
    this->unselect();
}

void ST7735::setImagesFullScreen(const uint16_t data[160][128]){
    for(uint16_t y = 0; y < this->HEIGHT; ++y){
        for(uint16_t x = 0; x < this->WIDTH; ++x){
            uint16_t color = ~data[y][x] & 0xFFFF;
            this->drawPixels(x, y, color);
        }
    }
}